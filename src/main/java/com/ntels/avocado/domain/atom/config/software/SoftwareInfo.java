package com.ntels.avocado.domain.atom.config.software;

public class SoftwareInfo {
	private String file_name;
	private String version;
	private String version_time;
	private String file_size;
	private String host_name;
	private String user_id;
	private String prc_date;
	private String prc_time;
	private String registered_date;
	
	public String getFile_name() {
		return file_name;
	}
	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getVersion_time() {
		return version_time;
	}
	public void setVersion_time(String version_time) {
		this.version_time = version_time;
	}
	public String getFile_size() {
		return file_size;
	}
	public void setFile_size(String file_size) {
		this.file_size = file_size;
	}
	public String getHost_name() {
		return host_name;
	}
	public void setHost_name(String host_name) {
		this.host_name = host_name;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getPrc_date() {
		return prc_date;
	}
	public void setPrc_date(String prc_date) {
		this.prc_date = prc_date;
	}
	public String getPrc_time() {
		return prc_time;
	}
	public void setPrc_time(String prc_time) {
		this.prc_time = prc_time;
	}
	public String getRegistered_date() {
		return registered_date;
	}
	public void setRegistered_date(String registered_date) {
		this.registered_date = registered_date;
	}
	
}
