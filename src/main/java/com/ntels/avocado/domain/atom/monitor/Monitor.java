package com.ntels.avocado.domain.atom.monitor;

import java.util.Arrays;

import com.ntels.avocado.domain.common.CommonCondition;

public class Monitor extends CommonCondition {
	
	private String search_type;
	private String search;
	private String[] packageName;
	private String nodeId;
	
	public String getSearch_type() {
		return search_type;
	}
	public void setSearch_type(String search_type) {
		this.search_type = search_type;
	}
	public String getSearch() {
		return search;
	}
	public void setSearch(String search) {
		this.search = search;
	}	
	public String[] getPackageName() {
		return packageName;
	}
	public void setPackageName(String[] packageName) {
		this.packageName = packageName;
	}	
	public String getNodeId() {
		return nodeId;
	}
	public void setNodeId(String nodeId) {
		this.nodeId = nodeId;
	}
	@Override
	public String toString() {
		return "Monitor [search_type=" + search_type + ", search=" + search
				+ ", packageName=" + Arrays.toString(packageName) + ", nodeId="
				+ nodeId + "]";
	}
	
	
	
}
