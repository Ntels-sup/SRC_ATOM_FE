public class TargetDomain {

	private String code;

	private String type_code;

	private String name;

	private String code_format;
	
	private String type_code_name;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getType_code() {
		return type_code;
	}

	public void setType_code(String type_code) {
		this.type_code = type_code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode_format() {
		return code_format;
	}

	public void setCode_format(String code_format) {
		this.code_format = code_format;
	}

	public String getType_code_name() {
		return type_code_name;
	}

	public void setType_code_name(String type_code_name) {
		this.type_code_name = type_code_name;
	}
	
}
