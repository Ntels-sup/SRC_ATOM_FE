package com.ntels.avocado.domain.atom.fault.alarmlevel;

public class AlmLevel {
	String monitor_id;
	String severity_ccd;
	String value;
	
	public String getMonitor_id() {
		return monitor_id;
	}
	public void setMonitor_id(String monitor_id) {
		this.monitor_id = monitor_id;
	}
	public String getSeverity_ccd() {
		return severity_ccd;
	}
	public void setSeverity_ccd(String severity_ccd) {
		this.severity_ccd = severity_ccd;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
}
