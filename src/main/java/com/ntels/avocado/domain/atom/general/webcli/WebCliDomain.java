package com.ntels.avocado.domain.atom.general.webcli;

import java.io.Serializable;

public class WebCliDomain implements Serializable {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -6656748049613148819L;
	
	
	public String getPkg_name() {
		return pkg_name;
	}
	public void setPkg_name(String pkg_name) {
		this.pkg_name = pkg_name;
	}
	public String getCmd_code() {
		return cmd_code;
	}
	public void setCmd_code(String cmd_code) {
		this.cmd_code = cmd_code;
	}
	private String pkg_name;
	private String cmd_code;
	private String package_id;
	private String system_id;
	private String user_id;	
	private String command;	
	private String argument;	
	private String gateway_ip;	
	private int seq;
	public String getPackage_id() {
		return package_id;
	}
	public void setPackage_id(String package_id) {
		this.package_id = package_id;
	}
	public String getSystem_id() {
		return system_id;
	}
	public void setSystem_id(String system_id) {
		this.system_id = system_id;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getCommand() {
		return command;
	}
	public void setCommand(String command) {
		this.command = command;
	}
	public String getArgument() {
		return argument;
	}
	public void setArgument(String argument) {
		this.argument = argument;
	}
	public String getGateway_ip() {
		return gateway_ip;
	}
	public void setGateway_ip(String gateway_ip) {
		this.gateway_ip = gateway_ip;
	}
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
	@Override
	public String toString() {
		return "WebCliDomain [pkg_name=" + pkg_name + ", cmd_code=" + cmd_code + ", package_id=" + package_id
				+ ", system_id=" + system_id + ", user_id=" + user_id + ", command=" + command + ", argument="
				+ argument + ", gateway_ip=" + gateway_ip + ", seq=" + seq + "]";
	}	
	
}

